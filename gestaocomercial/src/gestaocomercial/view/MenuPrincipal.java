    private void btnListVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListVendaActionPerformed
    StringBuilder sb = new StringBuilder();
    SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
    Date dataInicio = null, dataTermino = null;
    try {
         dataInicio = simpleDateFormat.parse(txtListVendaDataInicio.getText());
         dataTermino = simpleDateFormat.parse(txtListVendaDataTermino.getText());
    } catch (ParseException ex) {
//        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Data em formato inválido!");
        return;
    }
    dataTermino.setTime(dataTermino.getTime() + 86400000 - 1000);
    ArrayList<Venda> vendas = controleVendas.getVendas(dataInicio, dataTermino);
    for(Venda venda : vendas){
        sb.append(venda.toString());
    }
    txtListVenda.setText(sb.toString());
    }//GEN-LAST:event_btnListVendaActionPerformed

