    private void atualizarPreco(){
        int i, qtd_linhas = tabelaCadVendaProdutos.getRowCount();
        DefaultTableModel defaultTableModel = (DefaultTableModel) tabelaCadVendaProdutos.getModel();
        double preco, preco_total = 0;
        for(i = 0; i < qtd_linhas; i++){
            try{
//                System.out.println(((String) defaultTableModel.getValueAt(i, 5)).replace("R$", "").replace(",", "."));
                preco = Double.parseDouble(((String) defaultTableModel.getValueAt(i, 5)).replace("R$", "").replace(",", ".")) * ((Integer) defaultTableModel.getValueAt(i, 1));
            } catch(NumberFormatException ex){
                ex.printStackTrace();
                preco = 0;
            }
            preco_total += preco;
        }
        txtCadVendaPreco.setText(("R$" + new DecimalFormat("0.00").format(preco_total)).replace(".", ","));
    }
    
