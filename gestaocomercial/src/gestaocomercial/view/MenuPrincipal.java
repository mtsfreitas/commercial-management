    private void atualizarTelaCadVenda(int k){
        if(jTabbedPane4.getSelectedIndex() == 1){
            txtListVendaDataTermino.setText(new SimpleDateFormat("dd/MM/yyyy").format(new Date()));
        } else if(jTabbedPane4.getSelectedIndex() == 2){
            if(k != 1){
                comboVendaCliente.setModel(new javax.swing.DefaultComboBoxModel(controleClientes.getClientes(null).toArray()));
                comboVendaCliente.setRenderer(new ListCellRenderer<Object>() {
                    @Override
                    public Component getListCellRendererComponent(JList<? extends Object> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                        Object item = ((Cliente) value).getNome() + " - CPF: " + ((Cliente) value).getCPF() + 
                                (" - Saldo: R$" + new DecimalFormat("0.00").format(((Cliente) value).getSaldo())).replace(".", ",");
                        return new DefaultListCellRenderer().getListCellRendererComponent(list, item, index, isSelected, cellHasFocus);
                    }
                });
            }
            comboVendaEndereco.setModel(new javax.swing.DefaultComboBoxModel(((Cliente) comboVendaCliente.getSelectedItem()).getEnderecos().toArray()));
            comboVendaEndereco.setRenderer(new ListCellRenderer<Object>() {
                @Override
                public Component getListCellRendererComponent(JList<? extends Object> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                    Object item = ((Endereco) value).getEstado() + " - " + ((Endereco) value).getCidade() + " - " + 
                            ((Endereco) value).getRua() + " - " + ((Endereco) value).getComplemento();
                    return new DefaultListCellRenderer().getListCellRendererComponent(list, item, index, isSelected, cellHasFocus);
                }
            });
            comboVendaProduto.setModel(new javax.swing.DefaultComboBoxModel(controleProdutos.getProdutos(null).toArray()));
            comboVendaProduto.setRenderer(new ListCellRenderer<Object>() {
                @Override
                public Component getListCellRendererComponent(JList<? extends Object> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                    Object item = "Cod: " + ((Produto) value).getCodigo() + 
                            " - " + ((Produto) value).getDescricao() + (" - R$" + new DecimalFormat("0.00").format(((Produto) value).getPreco())).replace(".", ",") +
                            " - Estoque: " + ((Produto) value).getQtdEstoque();
                    return new DefaultListCellRenderer().getListCellRendererComponent(list, item, index, isSelected, cellHasFocus);
                }
            });
            atualizarPreco();
        }
    }
    
