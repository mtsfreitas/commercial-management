    private Cliente getDadosCliente(){
        String nome = txtCadClienteNome.getText();
        String cpf = txtCadClienteCPF.getText();
        int codigo = (Integer) spinCadClienteCodigo.getValue();
        String email = txtCadClienteEmail.getText();
        String senha = String.copyValueOf(txtCadClienteSenha.getPassword());
        String txt_enderecos = txtCadClienteEnderecos.getText();
        double saldo = Double.parseDouble(txtCadClienteSaldo.getText().replaceAll(",", "."));
        StringTokenizer st = new StringTokenizer(txt_enderecos, "\n");
        StringTokenizer st2;
        ArrayList<Endereco> enderecos = new ArrayList<>();
        String estado, cidade, rua, complemento;
        int numero;
        while(st.hasMoreTokens()){
            st2 = new StringTokenizer(st.nextToken(), "@");
            
            while(st2.hasMoreTokens()){
                estado = st2.nextToken();
                cidade = st2.nextToken();
                rua = st2.nextToken();
                numero = Integer.parseInt(st2.nextToken());
                complemento = st2.nextToken();
                enderecos.add(new Endereco(estado, cidade, rua, numero, complemento));
            }
        }
        Cliente cliente = new Cliente(codigo, cpf, nome, email, senha, enderecos, saldo);
        return cliente;
    }
    
