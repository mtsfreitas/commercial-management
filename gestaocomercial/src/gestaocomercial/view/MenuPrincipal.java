    private void btnCadVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadVendaActionPerformed
        Venda venda = this.getDadosVenda();
        double preco_total = Double.parseDouble(txtCadVendaPreco.getText().replace("R$", "").replace(",", "."));
        if(venda.getCliente().getSaldo() < preco_total){
            JOptionPane.showMessageDialog(null, "Saldo do cliente insuficiente!");
            return;
        }        
        Par<Boolean, String> resp_venda = controleVendas.efetuarVenda(venda, controleProdutos);
        JOptionPane.showMessageDialog(null, resp_venda.second);
        if(resp_venda.first == true){
            controleClientes.atualizarSaldo(venda.getCliente().getCodigo(), venda.getCliente().getSaldo() - preco_total);
            for(int i = tabelaCadVendaProdutos.getRowCount() - 1; i >= 0; i--)
                ((DefaultTableModel) tabelaCadVendaProdutos.getModel()).removeRow(i);
            atualizarTelaCadVenda(0);
        }        
    }//GEN-LAST:event_btnCadVendaActionPerformed

