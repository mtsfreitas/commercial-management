    private Venda getDadosVenda(){
        ArrayList<Par<Produto, Integer>> produtos_vendidos = new ArrayList<>();
        int i, qtd_linhas = tabelaCadVendaProdutos.getRowCount();
        DefaultTableModel defaultTableModel = (DefaultTableModel) tabelaCadVendaProdutos.getModel();
        for(i = 0; i < qtd_linhas; i++){
            produtos_vendidos.add(new Par<>((Produto) defaultTableModel.getValueAt(i, 8), (Integer) defaultTableModel.getValueAt(i, 1)));
        }
        
        if(produtos_vendidos.isEmpty()) // nenhum produto foi comprado...
            return null;
        
        return new Venda(
                (Integer) spinVendaCodVenda.getValue(),
                (Cliente) comboVendaCliente.getSelectedItem(),
                (Endereco) comboVendaEndereco.getSelectedItem(),
                new Date(),
                (StatusVenda) comboCadVendaStatus.getSelectedItem(),
                produtos_vendidos
        );
    }
